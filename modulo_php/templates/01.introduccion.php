<h1>01. Introducción al PHP</h1>
<ul>
    <li>
        PHP es el <strong>lenguaje de scripting</strong> del lado del servidor.
        Es uno de los más populares y se utiliza para crear sitios web dinámicos
        e interactivos.
    </li>
    
    <li>
        PHP es un lenguaje muy potente y escalable, fácil de aprender y usar.
    </li>
    
    <li>
         Los scripts PHP se ejecutan en el servidor web y el resultado se envía al navegador.
    </li>
    
    <li>
        PHP se puede integrar con la cantidad de bases de datos populares, incluidos 
<strong>MySQL</strong>, PostgreSQL, Oracle, Sybase, Informix y <strong>Microsoft SQL Server.</strong>
    </li>
    
</ul>

<p>
    PHP se puede incrustar dentro de las páginas web HTML normales de la siguiente forma:
</p>
    <pre>
        <code>
            &lt;!doctype HTML&gt;
            &lt;html&gt;
            &lt;head&gt;
                &lt;title&gt;Código PHP Incrustado&lt;/title&gt;
            &lt;/head&gt;
            &lt;body&gt;
                <strong>
                &lt;?php
                // Comentarios en PHP
                echo '¡Hola al Mundo PHP!';
                ?&gt;
                </strong>
            &lt;/body&gt;
            &lt;/html&gt;
        </code> 
    </pre>

<p>¿Qué podemos hacer con PHP?</p>

<ul>
    <li>
        Generar páginas dinámicas,
    </li>
    <li>
        Crear, abrir, leer, escribir y cerrar ficheros del servidor web.
    </li>
    <li>
        Recolectar información de usuarios a través de los formularios HTML.
    </li>
    
    <li>
        Enviar emails.
    </li>
    
    <li>
        Almacenar, eliminar y modificar información de bases de datos.
    </li>
    
    <li>
        Restringir acceso a páginas.
    </li>
    <li>
        ...........
    </li>
    
</ul>


